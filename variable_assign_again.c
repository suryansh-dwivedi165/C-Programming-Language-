#include<stdio.h>
int main(){
    int x = 5;  // value assign and initialization
    printf("%d\n",x);
    x = 7;      // value overwrite
    printf("%d\n",x);
    x = x + 6;  // value increament by 6
    printf("%d\n",x);
    x = x - 20;    // value decrement by 20
    printf("%d\n",x);
    return 0;
} 