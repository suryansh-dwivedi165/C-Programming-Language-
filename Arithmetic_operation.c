#include<stdio.h>
int main(){
    // Arithmetic operation on int data type 
    int x = 5;
    int y = 9;
    int add = x + y;
    printf("%d\n",add);
    int subtract = x - y;
    printf("%d\n",subtract);
    int multiplication = x * y;
    printf("%d\n",multiplication);
    int divide = x / y;
    printf("%d\n",divide);
    int remainder = x % y;
    printf("%d\n",remainder);
    return 0;
}  